    public static String lemmatize(String word) {
        String irr = irregularVerbMap.get(word);
        if (irr != null) return irr;
        //word = word.toLowerCase();
        if (verbCandSet.contains(word)) return word;
 
        if (word.endsWith("ing")) word = word.substring(0, word.length() - 3) + "e";
        else if (word.endsWith("ed")) word = word.substring(0, word.length() - 1);
        else if (word.matches(".*[^s]s")) word = word.substring(0, word.length() - 1);
        else return word;
        if (verbCandSet.contains(word)) return word;
 
        if (word.endsWith("ie")) return word.substring(0, word.length() - 2) + "y";
        if (word.endsWith("e")) word = word.substring(0, word.length() - 1);
        if (verbCandSet.contains(word)) return word;
        if (word.matches(".*[aeiou]([ndtmbpgklrz])\\1")) return word.substring(0, word.length() - 1);
        return word;
    }
     
  
